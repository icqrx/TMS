package jp.co.isb.tms.model;

public class DeliveryStatusInfo {
	
	private String mDeliveryStatusId;
	private String mDeliveryStatusName;

	public DeliveryStatusInfo() {

	}

	public String getDeliveryStatusId() {
		return mDeliveryStatusId;
	}

	public void setDeliveryStatusId(String mDeliveryStatusId) {
		this.mDeliveryStatusId = mDeliveryStatusId;
	}

	public String getDeliveryStatusName() {
		return mDeliveryStatusName;
	}

	public void setDeliveryStatusName(String mDeliveryStatusName) {
		this.mDeliveryStatusName = mDeliveryStatusName;
	}

	

}
